<script>
	gsap.to('.star-fixed .pic', {
		scrollTrigger: {
			toggleActions: "play pause resume reverse", //重覆觸發
			trigger: ".star-bannerWrap",
			start: "center 50%",
			endTrigger: ".buyitList",
			end: "center 50%",
			scrub: 0.6, //動畫綁定滾輪的延遲秒數(可做視差)
			pin: ".star-fixed",
			pinSpacing: false,  //pin起來會預留空間這可以取消
			// markers: true,
		},
		duration: 3,
		onUpdate: function () {
			var _p = this.progress()

			$("img", this._targets).css({
				filter: `drop-shadow(${120 * (1 - _p) * Math.sin(_p * 5)}px ${120 * (1 - _p) * Math.cos(_p * 5)}px 40px rgba(98, 59, 27, 0.5))`
			})
		}
	})



	$(window).scroll(function() {

		var scrollTop = $(this).scrollTop()
		var _b = scrollTop + $(window).height() * 0.8

		if (_b > $(".star-tipsWrap").offset().top) {
			$(".star-fixed").addClass("bg-hide")
			return false
		}else{
			$(".star-fixed").removeClass("bg-hide")
		}
	}).trigger("scroll")






	var $star_mobile = $(".mobile-star-sliderList").flickity({
		"prevNextButtons": false,
		"pageDots": false,
		"wrapAround": true,
		"autoPlay": 4000,
		"cellAlign": "center",
		"imagesLoaded": true,
		"arrowShape": ""
	});


	$(".mobile-star-foodWrap .prev").on("click", function () {
		$star_mobile.flickity('previous');
	})

	$(".mobile-star-foodWrap .next").on("click", function () {
		$star_mobile.flickity('next');
	})
</script>