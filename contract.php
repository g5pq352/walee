<script>
	
	$(".contract-fixed").on("click", function(){
		$(".contractCatsWrap").fadeIn(300)
	})
	$(".contractCats-close, .contractCatsWrap .bg").on("click", function(){
		$(".contractCatsWrap").fadeOut(300)
	})


	if(device == 'desktop'){
		var _windowH = $(".contract").offset().top
		$(window).on("scroll", () => {
			var _scrollTop = $(window).scrollTop()

			if (_scrollTop > _windowH * 0.9) {
				$(".topmenu-fixed").removeClass("is-white")
				return false;
			} else {
				$(".topmenu-fixed").addClass("is-white")
			}
		})
	}

	
	var $where = $("[data-where]").get().reverse()

	$(window).scroll(function() {

		var scrollTop = $(this).scrollTop()
		var _b = scrollTop + $(window).height() * 0.7

		$($where).each((i, el) => {

			var _index = el.dataset.where

			if (_b > $(el).offset().top) {
				$("#whereList li").eq(_index).addClass("current").siblings().removeClass("current")
				return false
			}else{
				$("#whereList li").removeClass("current")
			}
		})
	}).trigger("scroll")


	$("#whereList li").on("click", function () {
		gsap.to(window, {
			duration: 1.2,
			scrollTo: {
				y: $("[data-where=" + $(this).index() + "]"),
				offsetY: $(window).height() * 0.5,
			},
			ease: Power2.easeInOut
		});
	});
</script>